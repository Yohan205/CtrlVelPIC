/* Dibuja un bitmap almacenado por columnas.
   bitmap: datos columna por columna; cada columna consume (h+7)/8 bytes;
   bit 0 de cada byte corresponde a la fila superior de ese bloque de 8 pixels. */
void OLED_DrawBitmap(int16_t x, int16_t y, const uint8_t *bitmap, uint8_t w, uint8_t h, uint8_t color)
{
    if (bitmap == NULL)
        return;
    uint8_t bytesPerCol = (h + 7) / 8;
    for (uint8_t col = 0; col < w; col++)
    {
        for (uint8_t byteRow = 0; byteRow < bytesPerCol; byteRow++)
        {
            uint8_t b = *(bitmap + col * bytesPerCol + byteRow);
            for (uint8_t bit = 0; bit < 8; bit++)
            {
                uint8_t row = byteRow * 8 + bit;
                if (row >= h)
                    break; // fuera de alto solicitado
                int16_t px = x + col;
                int16_t py = y + row;
                if (b & (1 << bit))
                {
                    OLED_DrawPixel(px, py, color == INVERSE ? INVERSE : WHITE);
                }
                else
                {
                    if (color == INVERSE)
                        OLED_DrawPixel(px, py, WHITE);
                    else if (color == BLACK)
                        OLED_DrawPixel(px, py, BLACK);
                    else
                        ; // si color==WHITE y bit==0, dejamos como esta (no setear)
                }
            }
        }
    }
}

