{
    if (invert_status)
    {
        cfont.inverted = true;
    }
    else
    {
        cfont.inverted = false;
    }
}

void OLED_Update(void)
{
    uint16_t i = 0;
    uint8_t x = 0;
    ssd1306_command(SSD1306_SET_COLUMN_ADDR);
    ssd1306_command(0);
    ssd1306_command(SSD1306_LCDWIDTH - 1);
    ssd1306_command(SSD1306_SET_PAGE_ADDR);
    ssd1306_command(0);
#if SSD1306_LCDHEIGHT == 64
    ssd1306_command(7);
#endif
#if SSD1306_LCDHEIGHT == 32
    ssd1306_command(3);
#endif
#if SSD1306_LCDHEIGHT == 16
    ssd1306_command(1);
#endif
    /* Reusar un buffer temporal para evitar crear arrays en cada iteración */
    uint8_t data[17];
    data[0] = 0x40; // Control byte para datos
    const size_t total = (SSD1306_LCDWIDTH * SSD1306_LCDHEIGHT) / 8;
    size_t sent = 0;
    while (sent < total)
    {
        /* enviar hasta 16 bytes por transacción (data[0] es el control) */
        size_t remaining = total - sent;
        uint8_t chunk = (uint8_t)((remaining > 16) ? 16U : remaining);
        for (x = 0; x < chunk; x++)
        {
            data[1 + x] = buffer[sent + x];
        }
        /* Llamada al wrapper i2c_write */
        (void)i2c_write(SSD1306_ADDR, data, 1 + chunk);
        sent += chunk;
    }
}

