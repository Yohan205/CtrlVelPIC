void OLED_DrawPixel(int16_t x, int16_t y, uint8_t color)
{
    if ((x < 0) || (x >= SSD1306_LCDWIDTH) || (y < 0) || (y >= SSD1306_LCDHEIGHT))
        return;

    uint16_t page = (uint16_t)(y >> 3); /* y / 8 */
    uint16_t index = (uint16_t)(x + page * SSD1306_LCDWIDTH);
    uint8_t mask = (uint8_t)(1U << (y & 7));

    if (color == WHITE)
    {
        buffer[index] |= mask;
    }
    else if (color == BLACK)
    {
        buffer[index] &= (uint8_t)~mask;
    }
    else /* INVERSE */
    {
        buffer[index] ^= mask;
    }
}

void OLED_Write(int16_t x, int16_t y, char value)
{
    uint16_t font_idx = 0;
    uint16_t rowcnt = 0;
    uint16_t cnt = 0;
    uint8_t b;
    uint16_t temp = 0;
    int16_t cbyte, cx, cy;
    int16_t cbit;

    if (cfont.y_size % 8 == 0)
    {
        font_idx = ((value - cfont.offset) * (cfont.x_size * (cfont.y_size / 8))) + 4;
        /* Si el Y está alineado a página (múltiplo de 8), podemos escribir
           columnas completas directamente en el buffer en lugar de pixel a pixel. */
        if ((y & 7) == 0)
        {
            uint16_t pages = (uint16_t)(cfont.y_size / 8);
            for (cnt = 0; cnt < cfont.x_size; cnt++)
            {
                for (rowcnt = 0; rowcnt < pages; rowcnt++)
                {
                    temp = *(cfont.font + font_idx + cnt + (rowcnt * cfont.x_size));
                    uint16_t page = (uint16_t)((y >> 3) + rowcnt);
                    uint16_t index = (uint16_t)(x + cnt + page * SSD1306_LCDWIDTH);
                    if (cfont.inverted)
                    {
                        buffer[index] = (uint8_t)~temp;
                    }
                    else
                    {
                        buffer[index] = (uint8_t)temp;
                    }
                }
            }
        }
        else
        {
            /* Fallback: no alineado a pagina, mantener comportamiento por píxel */
            for (rowcnt = 0; rowcnt < (cfont.y_size / 8); rowcnt++)
            {
                for (cnt = 0; cnt < cfont.x_size; cnt++)
                {
                    temp = *(cfont.font + font_idx + cnt + (rowcnt * cfont.x_size));
                    for (b = 0; b < 8; b++)
                    {
                        int16_t px = (int16_t)x + (int16_t)cnt;
                        int16_t py = (int16_t)y + (int16_t)(rowcnt * 8) + (int16_t)b;
                        if (temp & (1 << b))
                        {
                            if (cfont.inverted == false)
                                OLED_DrawPixel(px, py, true);
                            else
                                OLED_DrawPixel(px, py, false);
                        }
                        else
                        {
                            if (cfont.inverted == false)
                                OLED_DrawPixel(px, py, false);
                            else
                                OLED_DrawPixel(px, py, true);
                        }
                    }
                }
            }
        }
    }
    else
    {
        font_idx = ((value - cfont.offset) * ((cfont.x_size * cfont.y_size) / 8)) + 4;
        cbyte = *(cfont.font + font_idx);
        cbit = 7;
        for (cx = 0; cx < cfont.x_size; cx++)
        {
            for (cy = 0; cy < cfont.y_size; cy++)
            {
                if ((cbyte & (1 << cbit)) != 0)
                {
                    if (cfont.inverted == false)
                        OLED_DrawPixel(x + cx, y + cy, true);
                    else
                        OLED_DrawPixel(x + cx, y + cy, false);
                }
                else
                {
                    if (cfont.inverted == false)
                        OLED_DrawPixel(x + cx, y + cy, false);
                    else
                        OLED_DrawPixel(x + cx, y + cy, true);
                }
                cbit--;
                if (cbit < 0)
                {
                    cbit = 7;
                    font_idx++;
                    cbyte = *(cfont.font + font_idx);
                }
            }
        }
    }
}

void OLED_Write_Text(int16_t x, int16_t y, char *text)
{
    size_t cnt;
    size_t length;
    length = strlen((const char *)text);
    if (x == RIGHT)
        x = SSD1306_LCDWIDTH - (int16_t)(length * cfont.x_size);
    if (x == CENTER)
        x = (SSD1306_LCDWIDTH - (int16_t)(length * cfont.x_size)) / 2;
    for (cnt = 0; cnt < length; cnt++)
        OLED_Write(x + (int16_t)(cnt * (cfont.x_size)), y, *text++);
}